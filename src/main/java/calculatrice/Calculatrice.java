package calculatrice;

class Calculatrice {

    int add(int a, int b) {
        return a + b;
    }

    int sub(int a, int b) {
        return a - b;
    }

    int mult(int a, int b) {
        return a * b;
    }

    int divis(int a, int b) {
        return a / b;
    }

    int reste(int a, int b) {
        return a % b;
    }
}
